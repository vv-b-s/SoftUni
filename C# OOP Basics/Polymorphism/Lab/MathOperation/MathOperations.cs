using System;
using System.Collections.Generic;
using System.Text;

public class MathOperations
{
    public int Add(int value1, int value2) => value1 + value2;

    public double Add(double value1, double value2, double value3) => value1 + value2 + value3;

    public decimal Add(decimal value1, decimal value2, decimal value3) => value1 + value2 + value3;

}